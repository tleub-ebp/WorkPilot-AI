/**
 * IPC Namespace Helpers — Architecture improvement 3.3
 *
 * Provides a namespace-grouped view of IPC channels and type-safe helpers.
 * The flat IPC_CHANNELS object is preserved for backward compatibility;
 * this module adds structure on top without breaking existing consumers.
 *
 * Usage:
 *   import { IPC_NS, getChannelsByNamespace } from '@shared/constants/ipc-namespaces';
 *
 *   // Typed namespace access
 *   ipcMain.handle(IPC_NS.project.ADD, ...);
 *   ipcMain.handle(IPC_NS.terminal.CREATE, ...);
 *
 *   // Get all channels for a domain (useful for auto-discovery / logging)
 *   const terminalChannels = getChannelsByNamespace('terminal');
 */

