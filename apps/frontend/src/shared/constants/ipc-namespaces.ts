export type IPCNamespace = keyof typeof IPC_NS;

// ============================================
// Utilities
// ============================================

/**
 * Returns all channel string values for a given namespace.
 * Useful for logging, debugging, or auto-registering handlers.
 *
 * @example
 *   getChannelsByNamespace('terminal')
 *   // => ['terminal:create', 'terminal:destroy', ...]
 */
export function getChannelsByNamespace(ns: IPCNamespace): ChannelValue[] {
  const group = IPC_NS[ns];
  return Object.values(group) as ChannelValue[];
}

/**
 * Extracts the namespace prefix from a channel string.
 *
 * @example
 *   getNamespaceFromChannel('terminal:create') // => 'terminal'
 *   getNamespaceFromChannel('github:pr:list')  // => 'github'
 */
export function getNamespaceFromChannel(channel: string): string {
  return channel.split(':')[0];
}

/**
 * Returns a flat list of all IPC channel values grouped by namespace.
 * Useful for documentation or runtime channel validation.
 */
export function getAllNamespacedChannels(): Record<string, ChannelValue[]> {
  const result: Record<string, ChannelValue[]> = {};
  for (const ns of Object.keys(IPC_NS) as IPCNamespace[]) {
    result[ns] = getChannelsByNamespace(ns);
  }
  return result;
}
