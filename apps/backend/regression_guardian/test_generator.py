class TestFramework(str, Enum):
    PYTEST = "pytest"
    UNITTEST = "unittest"
    JEST = "jest"
    VITEST = "vitest"
    MOCHA = "mocha"
    JUNIT5 = "junit5"
    TESTNG = "testng"
    GO_TEST = "go_test"
    XUNIT = "xunit"
    NUNIT = "nunit"
    CARGO_TEST = "cargo_test"


class TestStatus(str, Enum):
    RED = "red"
    GREEN = "green"
    SKIPPED = "skipped"


@dataclass
class GeneratedTest:
    """A regression test generated from an incident."""

    incident_id: str
    framework: TestFramework
    test_name: str
    test_code: str
    file_path: str
    status: TestStatus = TestStatus.RED
    target_file: str = ""
    target_function: str = ""
    fixtures: list[str] = field(default_factory=list)
    tags: list[str] = field(default_factory=list)


class TestGenerator:
    """Generate regression tests from production incidents.

    Usage::

        gen = TestGenerator()
        test = gen.generate(incident, framework=TestFramework.PYTEST)
    """

    def generate(
        self,
        incident: Incident,
        framework: TestFramework = TestFramework.PYTEST,
        project_root: str = "",
    ) -> GeneratedTest:
        """Generate a regression test for the given incident."""
        generators = {
            TestFramework.PYTEST: self._gen_pytest,
            TestFramework.UNITTEST: self._gen_unittest,
            TestFramework.JEST: self._gen_jest,
            TestFramework.VITEST: self._gen_vitest,
            TestFramework.MOCHA: self._gen_mocha,
            TestFramework.JUNIT5: self._gen_junit5,
            TestFramework.GO_TEST: self._gen_go_test,
            TestFramework.XUNIT: self._gen_xunit,
            TestFramework.CARGO_TEST: self._gen_cargo_test,
        }
        gen_fn = generators.get(framework, self._gen_pytest)
        return gen_fn(incident, project_root)

    def detect_framework(self, project_files: list[str]) -> TestFramework:
        """Detect the test framework from project file listing."""
        file_set = set(f.lower() for f in project_files)

        if "pytest.ini" in file_set or "pyproject.toml" in file_set or "conftest.py" in file_set:
            return TestFramework.PYTEST
        if "jest.config.js" in file_set or "jest.config.ts" in file_set:
            return TestFramework.JEST
        if "vitest.config.ts" in file_set or "vitest.config.js" in file_set:
            return TestFramework.VITEST
        if "pom.xml" in file_set or "build.gradle" in file_set:
            return TestFramework.JUNIT5
        if "go.mod" in file_set:
            return TestFramework.GO_TEST
        if "cargo.toml" in file_set:
            return TestFramework.CARGO_TEST
        if any(f.endswith(".csproj") for f in file_set):
            return TestFramework.XUNIT
        if ".mocharc.yml" in file_set or ".mocharc.js" in file_set:
            return TestFramework.MOCHA

        return TestFramework.PYTEST

